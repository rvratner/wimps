<h2>Bio:</h2>

<p>Seattle's punk rock's slackers in chief wimps are a Seattle-based three-piece comprised of scene veterans Rachel Ratner (Butts) on guitar and lead vocals, Matt Nyce (Meth Teeth) on bass and backing vocals, and Dave Ramm (The Intelligence) on drums.</p>

<p>Wimps serve their own brand of rapid-fire punk with a smattering of lo-fi post punk guitar work to create an angular, slightly off-kilter blast.</p>

<p>Ratner lyrically twists otherwise common day-to-day woes into relatable tales of struggle shout-sung call and response stabs that champion boredom, laziness and social anxieties with wiry riffs and punchy bass lines, propelled by the measured rhythms of drummer David Ramm.</p>

<p>After gaining local momentum from a self released demo, the band signed with End of Time Records and issued their first LP â€œRepeat" in 2013. They toured nationwide supporting the album, receiving coverage on This American Life, Pitchfork, SPIN and KEXP. Following their return, they released a 7 inch on micro label Help Yourself Records and a series of self released tapes.</p>

<p>In 2015 they partnered with Kill Rock Stars to release "Super Me" is a 5 song cassette EP available August 7th, followed by LP which will be released later in the year.</p>

